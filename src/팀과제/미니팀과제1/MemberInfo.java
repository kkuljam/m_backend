package 팀과제.미니팀과제1;

public class MemberInfo {
    int no;
    String name;
    String id;
    String pw;
    String num;
    String birth;

    MemberInfo(int no, String name, String id, String pw, String num, String birth){
        this.no=no;
        this.name=name;
        this.id=id;
        this.pw=pw;
        this.num=num;
        this.birth=birth;
    }
}
